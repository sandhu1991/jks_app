export function bookMailto(packageTitle) {
  return `mailto:${site.email}?subject=${encodeURIComponent(`Booking request: ${packageTitle}`)}`
}

/** Cal.com booking link, or mailto fallback if username/slug not set. */
export function bookLink({ title, calSlug, bookingUrl }) {
  if (bookingUrl) return bookingUrl
  if (site.calComUsername && calSlug) {
    return `https://cal.com/${site.calComUsername}/${calSlug}`
  }
  return bookMailto(title)
}

